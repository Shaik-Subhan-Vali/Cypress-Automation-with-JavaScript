def compare_operations(num1, num2):
    if num1 > num2:
        print(f"{num1} is larger than {num2}")
    elif num1 < num2:
        print(f"{num1} is larger than {num2}")
    else:
        print("Both numbers are equal")
# Prompting to input num1
num1 = input("Enter your num1: ")
# Prompting to input num2
num2 = input("Enter your num2: ")

# Larger Number Identifier
compare_operations(num1, num2)    